import openmc

water = openmc.Material(name="Water with 1384 PPM Boron")
water.add_nuclide('H1', 6.6737e-02)
water.add_nuclide('O16', 3.3369e-02)
water.add_nuclide('B10', 1.5359e-05)
water.add_nuclide('B11', 6.1824e-05)
water.add_s_alpha_beta('c_H_in_H2O')

fuel = openmc.Material(name="Fuel (2.459 w/o with B-10 for impurities)")
fuel.add_nuclide('U234', 4.5689e-06)
fuel.add_nuclide('U235', 5.6868e-04)
fuel.add_nuclide('U238', 2.2268e-02)
fuel.add_nuclide('O16', 4.5683e-02)
fuel.add_nuclide('B10', 2.6055e-07)

aluminum = openmc.Material(name="Aluminum 6061 Cladding for Fuel")
aluminum.add_element('Mg', 6.2072e-04)
aluminum.add_element('Al', 5.3985e-02)
aluminum.add_element('Si', 3.2230e-04)
aluminum.add_element('Ti', 4.7263e-05)
aluminum.add_element('Cr', 5.8029e-05)
aluminum.add_element('Mn', 4.1191e-05)
aluminum.add_element('Fe', 1.8910e-04)
aluminum.add_element('Cu', 5.9353e-05)
aluminum.add_element('Zn', 5.7679e-05)

al2o3 = openmc.Material(name="Al2O3")
al2o3.set_density('sum')
al2o3.add_nuclide('O16', 2.3036e-02)
al2o3.add_nuclide('Al27', 1.5357e-02)

cladding = openmc.Material(name="Cladding for Al2O3 Perturbing Rods")
cladding.add_element('Mg', 6.6898e-04)
cladding.add_element('Al', 5.8183e-02)
cladding.add_element('Si', 3.4736e-04)
cladding.add_element('Ti', 5.0939e-05)
cladding.add_element('Cr', 6.2542e-05)
cladding.add_element('Mn', 4.4395e-05)
cladding.add_element('Fe', 2.0380e-04)
cladding.add_element('Cu', 6.3968e-05)
cladding.add_element('Zn', 6.2164e-05)

materials = openmc.Materials([water, fuel, aluminum, al2o3, cladding])
materials.export_to_xml()
